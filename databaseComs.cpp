std::vector<Character> DatabaseComs::getCharacters(){
    QSqlQuery query;
    std::string command = "SELECT characterId, name, totalKillCount FROM Character";
    std::vector<int> charIds;
    std::vector<std::string> charNames;
    std::vector<int> killCounts;
    query.exec(QString::fromStdString(command));
    while(query.next()){ // extract data
        charIds.push_back(query.value(0).toInt()); // characterId
        charNames.push_back(query.value(1).toString().toStdString()); // name
        killCounts.push_back(query.value(2).toInt()); // totalKillCount
    }
    std::vector<Character> charList;
    for (int i = 0; i < charIds.size(); ++i){ // build characters
        Character newChar;
        newChar.setName(charNames[i]); // Set name for character
        charList.push_back(newChar); // Add new character to list
        charList[i].addKill(killCounts[i]);
    }
    assignItemsToChars(charList, charIds); // Add items to character // Needs testing
    assignEntities(charList, charIds); // Add allies to character // Needs testing
    // TODO: Add entities to characters
    return charList;
}

