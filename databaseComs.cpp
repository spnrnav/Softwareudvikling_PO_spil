int DatabaseComs::execute(std::string sql){
    QSqlQuery query;

    if (!query.exec(QString::fromStdString(sql))) {
        std::cerr << "SQL Error: "
                  << query.lastError().text().toStdString()
                  << std::endl;
        return -1;
    }

    //return true;
    return query.lastInsertId().toInt();
}

void DatabaseComs::createTables(){
    std::string karakter =
        "CREATE TABLE IF NOT EXISTS Character ("
        "characterId	INTEGER,"
        "name	TEXT,"
        "totalKillCount	INTEGER,"
        "itemMostUsed	TEXT,"
        "entityMostUsed	TEXT,"
        "PRIMARY KEY(characterId)"
        ");"; // 'karakter' used instead of 'character' to avoid potential keyword conflicts
    
    std::string entity =
        "CREATE TABLE IF NOT EXISTS Entity ("
        "entityId   INTEGER,"
        "characterId	INTEGER,"
        "entityIdx	INTEGER,"
        "PRIMARY KEY(entityId),"
        "FOREIGN KEY(characterId) REFERENCES Character(characterId)"
        ");";

    std::string entItem =
        "CREATE TABLE IF NOT EXISTS EntItem ("
        "itemIdx	INTEGER,"
        "entityId	INTEGER,"
        "itemKills	INTEGER,"
        "itemUses   INTEGER,"
        "FOREIGN KEY(entityId) REFERENCES Entity(entityId)"
        ");";
    
    std::string charItem =
        "CREATE TABLE IF NOT EXISTS CharItem ("
        "itemIdx	INTEGER,"
        "characterId	INTEGER,"
        "FOREIGN KEY(characterId) REFERENCES Character(characterId)"
        ");";
    
    execute(karakter);
    execute(entity);
    execute(entItem);
    execute(charItem);
}

